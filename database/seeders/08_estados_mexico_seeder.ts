function getRegionKey(s: DemoListing): string {
  const state = s.state.toLowerCase()
  if (state.includes('méxico')) return 'cdmx'
  if (state.includes('yucatán')) return 'merida'
  if (state.includes('guanajuato')) return 'sma'
  if (state.includes('quintana roo')) return 'tulum_playa'
  if (state.includes('querétaro')) return 'queretaro'
  if (state.includes('jalisco') && s.municipality.includes('Guadalajara')) return 'guadalajara'
  if (state.includes('jalisco') && s.municipality.includes('Vallarta')) return 'vallarta'
  if (state.includes('nuevo león')) return 'monterrey'
  return 'cdmx' // default
}

function buildPhotoUrls(s: DemoListing): string[] {
  const regionKey = getRegionKey(s) as keyof typeof PHOTO_POOLS
  const pool = PHOTO_POOLS[regionKey] || PHOTO_POOLS.cdmx
  const start = hashStr(s.slug) % pool.length
  return Array.from({ length: s.photoCount }, (_, i) => {
    return pool[(start + i) % pool.length]
  })
}

export default class extends BaseSeeder {
  public async run() {
    const agent = await User.query().where('role', 'publisher').first()
    const agentId = agent?.id ?? null

    const ALL_LISTINGS = [
      ...CDMX,
      ...MERIDA,
      ...SMA,
      ...TULUM_PLAYA,
      ...QUERETARO,
      ...GUADALAJARA,
      ...MONTERREY,
      ...VALLARTA,
    ]

    for (const s of ALL_LISTINGS) {
      const photoUrls = buildPhotoUrls(s)

      const listing = await Listing.updateOrCreate(
        { slug: s.slug },
        {
          slug: s.slug,
          title: s.title,
          summary: s.summary,
          operationType: s.operationType,
          propertyType: s.propertyType,
          price: s.price,
          priceLabel: s.priceLabel,
          beds: s.beds,
          baths: s.baths,
          parking: s.parking,
          m2Built: s.m2Built,
          m2Land: s.m2Land,
          age: s.age,
          amenities: s.amenities,
          address: s.address,
          zone: s.zone,
          colony: s.colony,
          municipality: s.municipality,
          state: s.state,
          lat: s.lat,
          lng: s.lng,
          status: 'published',
          isPremier: s.isFeatured,
          isFeatured: s.isFeatured,
          badges: s.badges,
          highlights: s.highlights,
          mediaCount: s.photoCount,
          coverImageUrl: photoUrls[0] ?? null,
          publishedAt: DateTime.now(),
          viewCount: 0,
          agentId,
          createdBy: agentId,
          bedsLabel: s.beds ? `${s.beds} rec.` : null,
          sizeLabel: s.m2Built
            ? `${s.m2Built} m² const.${s.m2Land ? ` · ${s.m2Land} m² terreno` : ''}`
            : s.m2Land
              ? `${s.m2Land} m² terreno`
              : null,
          sizeM2: s.m2Built ? Math.round(s.m2Built) : s.m2Land ? Math.round(s.m2Land) : null,
        }
      )

      await ListingPhoto.query().where('listing_id', listing.id).delete()
      for (const [i, photoUrl] of photoUrls.entries()) {
        await ListingPhoto.create({
          listingId: listing.id,
          url: photoUrl,
          sortOrder: i + 1,
          isCover: i === 0,
        })
      }
    }
  }
}
